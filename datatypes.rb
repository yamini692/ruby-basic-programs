# Ruby Data Types Demonstration

# String
name = "Ruby"
puts "String: #{name}"

# Integer
age = 25
puts "Integer: #{age}"

# Float
price = 99.99
puts "Float: #{price}"

# Boolean
is_learning = true
puts "Boolean: #{is_learning}"

# Array
fruits = ["Apple", "Banana", "Cherry"]
puts "Array: #{fruits}"

# Hash
person = { "name" => ["Alice","jhon"], "age" => 30, "city" => "Chennai" }
puts "Hash: #{person}"

# Symbol
status = :active
puts "Symbol: #{status}"

# Nil
unknown_value = nil
puts "Nil: #{unknown_value.nil?}" 

